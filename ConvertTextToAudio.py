# บันทึกไฟล์เสียงที่ปรับความเร็วแล้ว
new_output_path = "ป้อมปราการหัวใจ.mp3"
new_audio.export(new_output_path, format="mp3")

print(f"ไฟล์เสียงถูกบันทึกที่: {new_output_path}")
