# สร้างเสียงอ่านจากบทความ
tts = gTTS(text, lang='th')
output_path = "ai_automation_cant_replace_humans.mp3"
tts.save(output_path)

# โหลดไฟล์เสียง
audio = AudioSegment.from_file(output_path)

# ปรับความเร็ว
speed = 1.25  # ปรับเป็นค่าใดก็ได้ที่ต้องการ, เช่น 0.75 สำหรับช้าลง หรือ 1.5 สำหรับเร็วขึ้น
